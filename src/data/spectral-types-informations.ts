const spectralTypeTable: { [key: string]: Kelvin } = rgbBySpectralType
  .map(([spectralType, value]) => map(mkKelvin(value), (kelvin) => [spectralType, kelvin]))
  .reduce((acc, pair) => {
    if (isError(pair)) {
      return acc;
    } else {
      return { ...acc, [pair[0]]: pair[1] };
    }
  }, {});

export function findTemperatureOf(spectralType: string): Kelvin | null {
  const maybeSpectralTypePrincipal = spectralType.match(/^([A-Za-z0-9]+)/);

  if (!maybeSpectralTypePrincipal) {
    return null;
  } else {
    const extractSpectralType = maybeSpectralTypePrincipal[0];
    if (!spectralTypeTable[extractSpectralType] && extractSpectralType.length === 2) {
      const completedSpectralType = extractSpectralType + 'Ia';
      return spectralTypeTable[completedSpectralType] || null;
    }

    return spectralTypeTable[extractSpectralType] || null;
  }
}

const spectralTypeRgbTable: { [key: string]: [number, number, number] } = rgbBySpectralType.reduce((acc, pair) => {
  if (isError(pair)) {
    return acc;
  } else {
    return { ...acc, [pair[0]]: pair[2] };
  }
}, {});

const white: [number, number, number] = [255, 255, 255];
export function findColorOf(spectralType: string): [number, number, number] {
  const maybeSpectralTypePrincipal = spectralType.match(/^([A-Za-z0-9]+)/);

  if (!maybeSpectralTypePrincipal) {
    return white;
  } else {
    const extractSpectralType = maybeSpectralTypePrincipal[0];

    return spectralTypeRgbTable[extractSpectralType] || white;
  }
}
